    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if(salvarAlterar.equals("novo")){
        modelUsuario.setUsuLogin(txtNomeUsu.getText());
        modelUsuario.setUsuSenha(txtSenha.getText());
        modelUsuario.setUsuNome(txtNomeCom.getText());        
        if(controllerUsuario.salvarUsuariosController(modelUsuario)>0){
            JOptionPane.showMessageDialog(this, "Usuario Cadastrado com sucesso");
            limparCampos();
            habilitarDesabilitarCampos(false);
            carregarUsuarios();
        }else if(salvarAlterar.equals("alterar")){
            alterarUsuario();
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuario");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
    }
