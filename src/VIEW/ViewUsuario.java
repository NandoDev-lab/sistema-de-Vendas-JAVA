    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         int linha = tblCadastrados.getSelectedRow();
        int codigoCliente = (int) tblCadastrados.getValueAt(linha, 0);
        try {
            if (controllerUsuario.excluirUsuariosController(codigoCliente)) {
                JOptionPane.showMessageDialog(this, "Registro excluido com sucesso!");
                carregarUsuarios();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nenhum registro foi selecionado ou Excluido", "Aviso", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

