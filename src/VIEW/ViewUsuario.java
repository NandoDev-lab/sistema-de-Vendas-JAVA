    private void alterarUsuario(){
        salvarAlterar = "alterar";
        int linha = tblCadastrados.getSelectedRow();
        int codigoCliente = (int) tblCadastrados.getValueAt(linha, 0);
        
        modelUsuario = controllerUsuario.getUsuariosController(codigoCliente);
        txtCodigo.setText(String.valueOf(this.modelUsuario.getIdUsuario()));
        txtNomeCom.setText(modelUsuario.getUsuNome());
        txtNomeUsu.setText(modelUsuario.getUsuLogin());
        txtSenha.setText(modelUsuario.getUsuSenha());
        
    }

    
    
    private void carregarUsuarios(){
        listaModelUsuario = controllerUsuario.getListaUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) tblCadastrados.getModel();
        modelo.setNumRows(0);
        
        int cont =listaModelUsuario.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
            listaModelUsuario.get(i).getIdUsuario(),
            listaModelUsuario.get(i).getUsuLogin(),
            listaModelUsuario.get(i).getUsuNome(),
            listaModelUsuario.get(i).getUsuSenha()          
            });            
        }
    }
    private void limparCampos(){
        txtCodigo.setText("");
        txtNomeCom.setText("");
        txtNomeUsu.setText("");
        txtSenha.setText("");
    }
    
    private void habilitarDesabilitarCampos(boolean condicao){
        txtCodigo.setEnabled(condicao);
        txtNomeCom.setEnabled(condicao);
        txtNomeUsu.setEnabled(condicao);
        txtSenha.setEnabled(condicao);
    }

