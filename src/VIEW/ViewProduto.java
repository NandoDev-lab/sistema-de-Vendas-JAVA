    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.tblProdutos.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.tblProdutos.setRowSorter(classificador);
        String texto = txtPesquisar.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto,1));
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterado = "alterar";
        habilitarDesabilitarCampos(true);
        int linha  = tblProdutos.getSelectedRow();
       
        try {
            int codigoProduto = (int) tblProdutos.getValueAt(linha, 0);
            /**recuperar oss dados do banco*/
            modelProdutos = controllerProdutos.retornarProdutosController(codigoProduto);
            /**preencher na interface*/
            this.txtCodigo.setText(String.valueOf(modelProdutos.getIdProduto()));
            this.txtNomeProd.setText(modelProdutos.getProdNome());
            this.txtEstoque.setText(String.valueOf(modelProdutos.getProEstoque()));
            this.txtValor.setText(String.valueOf(modelProdutos.getProdValor()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"codigo invalido ou nenhum registro selecionado","Aviso",JOptionPane.ERROR_MESSAGE);
        }        
        
    }//GEN-LAST:event_btnAlterarActionPerformed

           
    
    
    
    
    
    
    
    
    
