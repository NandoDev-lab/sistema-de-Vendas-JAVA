    private void salvarProduto(){
        modelProdutos.setProdNome(this.txtNomeProd.getText());
        modelProdutos.setProEstoque(Integer.parseInt(this.txtEstoque.getText()));
        modelProdutos.setProdValor(formatador.converterVirgulaParaPontoReturn( this.txtValor.getText()));
        if(controllerProdutos.salvarProdutosContoller(modelProdutos)>0){
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso");
            limparCampos();
            habilitarDesabilitarCampos(false);
            this.carregarProdutos();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Salvar um novo produto","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }
    private void alterarProduto(){
        modelProdutos.setProdNome(this.txtNomeProd.getText());
        modelProdutos.setProEstoque(Integer.parseInt(this.txtEstoque.getText()));
        modelProdutos.setProdValor(formatador.converterVirgulaParaPontoReturn( this.txtValor.getText()));
        if(controllerProdutos.alterarProdutoController(modelProdutos)){
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso");
            limparCampos();
            habilitarDesabilitarCampos(false);
            this.carregarProdutos();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Alterar um produto","ERRO",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**limpa os campos do formulario: nome, estoque e valor*/
    private void limparCampos(){
        txtNomeProd.setText("");
        txtEstoque.setText("");
        txtValor.setText("");
    }
    
    /**habilita e desabilita campos para edição*/
    private void habilitarDesabilitarCampos (boolean condição){
        txtNomeProd.setEnabled(condição);
        txtEstoque.setEnabled(condição);
        txtValor.setEnabled(condição);
    }
    
    
    /**preenche a tabela de produtos com os produtos do banco */
    private void carregarProdutos(){
     listaModelProdutos = controllerProdutos.retornarListaProdutosController();
     DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
     modelo.setNumRows(0);
     /** inserir produtos na tabela*/
     int cont = listaModelProdutos.size();
     for (int i =0;i<cont;i++){
        modelo.addRow(new Object[]{
           listaModelProdutos.get(i).getIdProduto(),
           listaModelProdutos.get(i).getProdNome(),
           listaModelProdutos.get(i).getProEstoque(),
           listaModelProdutos.get(i).getProdValor(),
        });       
     }     
    }

